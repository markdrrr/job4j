package ru.job4j.professions;

public class Profession {
    String name;
    String surname;
    String education;
    long birthday;

    public String getName() {
        return  name;
    }

    public String getSurname() {
        return  surname;
    }

    public String getEducation() {
        return  education;
    }

    public long getBirthday() {
        return  birthday;
    }
}
